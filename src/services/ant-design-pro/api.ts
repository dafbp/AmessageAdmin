import { request } from 'umi';
import axios from 'axios';
import { config, domain } from '../api/axios';

export const USER_API = {
  login: async ({ user, password }: { user: string; password: string }) => {
    try {
      const resp = await axios.post(`${domain}login`, { user, password });
      console.log('Đăng nhập ', resp);
      return {
        data: resp.data,
        success: resp.data.status === 'success',
      };
    } catch (err) {
      // Handle Error Here
      console.error(err);
      return {
        error: err,
      };
    }
  },
};

/** Get current users GET /api/currentUser */
export async function currentUser(options?: { [key: string]: any }) {
  return request<{
    data: API.CurrentUser;
  }>('/api/currentUser', {
    method: 'GET',
    ...(options || {}),
  });
}

/** Exit login interface POST /api/login/outLogin */
export async function outLogin(options?: { [key: string]: any }) {
  return request<Record<string, any>>('/api/login/outLogin', {
    method: 'POST',
    ...(options || {}),
  });
}

/** Login interface POST /api/login/account */
export async function login(body: API.LoginParams, options?: { [key: string]: any }) {
  return request<API.LoginResult>('/api/login/account', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    data: body,
    ...(options || {}),
  });
}

